"""
Aqui probaremos el parametro sep de print
"""

print("Esto es un print normal\n")
print("1","2","3","4","5")
print("Ahi el separador es un espacio por defecto\n")

print("Aqui no habra separador por defecto.")
print("1","2","3","4","5", sep="")

print("\nAqui el separador es un slash \"/\"")
print("1","2","3","4","5", sep="/")

